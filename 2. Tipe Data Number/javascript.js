//number tidak perlu menggunakan kutip (""), langsung numbernya saja.
document.writeln(100);
document.writeln("</br>");
//bilangan pecahan di javascript tidak mengunakan koma(,) tapi titik(.)
document.writeln(100.123);
document.writeln("</br>");
// number notation
/* javascript juga mendukung binary, hexadecimal dan octal */

//  binary => 0b10101
document.writeln(0b10101);
document.writeln("</br>");
//  hexadecimal => 0xFF
document.writeln(0xff);
document.writeln("</br>");
// octal => 0o10
document.writeln(0o10);
